					<p>Indoor air pollution (IAP) resulting from the burning of traditional solid fuels, such as fire wood and cow-dungin inefficient cook stoves is recognized as a serious global public health threat. Recently, it has been estimated that over 3 billion people rely on solid fuels and other biomass for cooking and heating, and most use traditional stoves that are very inefficient in converting wood into heat while cooking food (Burki 2011; WHO 2011). World Health Report identifies IAP as the single most environmental risk factor for female mortality, attributing 5% of all female deaths in the developing world to indoor smoke (WHO 2002). Epidemiological studies reported powerful associations between IAP exposure and acute respiratory infection (ARI) symptoms.  Despite these large health hazards, half of the world’s population and over 75% of South Asians continue to rely on dung, brush, and wood as their primary source of energy for cooking and heating [Smith et al. 2004; Ezzati and Kammen 2001; Pokhrel 2005). In general, the combustion products of wood are carbon dioxide, water vapour and carbon monoxide, particulate and polycyclic organic matters, the last three of which are known to be pollutants hazardous to human health (Arif et al. 2011). Most of the stoves were found to be of traditional type including some improved cook stoves (ICS) where biomass and agricultural residues were the major sources of fuels to be used (Dey NC, Ali Arif T, Ahmed 2017). Study identified that reportedly, most of the women of the households suffered mainly from eye problem, ARI symptoms, i.e. breathing problem, dry cough because of greater exposure to IAP during cooking using. The respondents were generally aware of indoor air pollution and its harmful effects but attributed this more to fuel than to stove. As such, changing fuel was their solution to prevent IAP, not stove. The logistic regression analysis showed that the prevalence of health symptoms were significantly associated with exposure to IAP, women compared to male and higher age of the household members, traditional stove types compared to ICS and positive smoking condition (Dey NC, Ali and Ahmed 2017).</p>
