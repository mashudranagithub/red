					<p>The Sustainable Development Goal (SDG 2), “End hunger, achieve food security and improved nutrition and promote sustainable agriculture”, calls for collective, inclusive and multidisciplinary action to address the complex root-causes of hunger and malnutrition. The implementation of the 2030 Agenda for Sustainable Development is driven by governments, with the support of all stakeholders, and progress will be reviewed through an inclusive process at national, regional and global levels, converging to the High Level political Forum (HLPF) annual meetings.</p>
					<p>At the core of human health is nutrition. Nutrition and food security is achieved when “all people, at all times, have physical and economic access to sufficient, safe and nutritious food to meet their dietary needs and food preferences for an active and healthy life” (FAO, 1996). The absence of food and nutrition security can have significant short and long term consequences for individuals and for society, including malnutrition, obesity, disease, and poverty. The progress in food utilization, the third dimension of food security is still unclear in many countries including Bangladesh due to lack of evidence on people’s knowledge on proper diet, and on food safety and hygiene; which are essential to conserve their nutritious value. Recently study identified that most of the consumers—food purchaser, cook, adolescent and caregivers of 6-23 months old child could give partial correct answers to most of the basic nutrition related questions, and overall one-third of the consumers living in rural and urban areas have low basic nutrition knowledge level, and comparatively much lower in slum areas (Dey et al. 2021).</p>
					<p>Work within food and nutritional security aims to address both underlying and proximate determinants of food and nutritional security, including access to foods, care and feeding practices, and access to health care and treatment of illness. In line with government efforts to achieve food security and improved nutrition, RED has conducted a number of studies to identify determinants of food security and nutrition and some policy solutions. Studies show that to achieve food security and improved nutritional in different environmental, social and economic aspects, emphasis can be given on improving consumer awareness of nutrition, food safety and hygiene including child feeding related knowledge (IYCF) of caregiver (Dey et al. 2021). Researchers employ a systems perspective to study and understanding the complex drivers of nutrition and seek to identify effective pathways for development interventions to improve nutrition among marginalized and vulnerable populations.</p>
